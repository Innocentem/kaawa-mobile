-- 7. Interested Buyers table
create table if not exists public.interested_buyers (
  id bigint generated by default as identity primary key,
  coffee_stock_id bigint references public.coffee_stock(id) on delete cascade not null,
  buyer_id uuid references public.profiles(id) on delete cascade not null,
  seen_by_farmer boolean default false,
  created_at timestamptz default now(),
  unique(coffee_stock_id, buyer_id)
);

alter table public.interested_buyers enable row level security;

do $$
begin
  if not exists (select 1 from pg_policies where policyname = 'Users can see interests related to them.' and tablename = 'interested_buyers') then
    create policy "Users can see interests related to them." on public.interested_buyers for select using (
      auth.uid() = buyer_id or
      exists (
        select 1 from public.coffee_stock
        where id = coffee_stock_id and farmer_id = auth.uid()
      )
    );
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Buyers can express interest.' and tablename = 'interested_buyers') then
    create policy "Buyers can express interest." on public.interested_buyers for insert with check (auth.uid() = buyer_id);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Farmers can update seen status.' and tablename = 'interested_buyers') then
    create policy "Farmers can update seen status." on public.interested_buyers for update using (
      exists (
        select 1 from public.coffee_stock
        where id = coffee_stock_id and farmer_id = auth.uid()
      )
    );
  end if;
end $$;

