end $$;

-- 3. Messages table
create table if not exists public.messages (
  id bigint generated by default as identity primary key,
  sender_id uuid references public.profiles(id) on delete cascade not null,
  receiver_id uuid references public.profiles(id) on delete cascade not null,
  text text not null,
  coffee_stock_id bigint references public.coffee_stock(id) on delete set null,
  is_read boolean default false,
  is_purchase_request boolean default false,
  purchase_request_data jsonb,
  created_at timestamptz default now()
);

alter table public.messages enable row level security;

do $$
begin
  if not exists (select 1 from pg_policies where policyname = 'Users can see their own messages.' and tablename = 'messages') then
    create policy "Users can see their own messages." on public.messages for select using (auth.uid() = sender_id or auth.uid() = receiver_id);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Users can send messages.' and tablename = 'messages') then
    create policy "Users can send messages." on public.messages for insert with check (auth.uid() = sender_id);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Users can update is_read status.' and tablename = 'messages') then
    create policy "Users can update is_read status." on public.messages for update using (auth.uid() = receiver_id);
  end if;
end $$;

