begin
  -- Policy for public access to media
  if not exists (select 1 from pg_policies where policyname = 'Public Access' and tablename = 'objects' and schemaname = 'storage') then
    create policy "Public Access" on storage.objects for select using (bucket_id = 'kaawa-media');
  end if;

  -- Policy for users to upload their own profile pictures (path: profiles/{user_id}/{filename})
  if not exists (select 1 from pg_policies where policyname = 'Users can upload their own profile pictures' and tablename = 'objects' and schemaname = 'storage') then
    create policy "Users can upload their own profile pictures" on storage.objects for insert with check (
      bucket_id = 'kaawa-media' and (storage.foldername(name))[1] = 'profiles' and (storage.foldername(name))[2] = auth.uid()::text
    );
  end if;

