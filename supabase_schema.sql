  handled boolean default false,
  handled_at timestamptz,
  handled_by_admin uuid references public.profiles(id),
  created_at timestamptz default now()
);

alter table public.password_resets enable row level security;

do $$
begin
  if not exists (select 1 from pg_policies where policyname = 'Anyone can request a password reset.' and tablename = 'password_resets') then
    create policy "Anyone can request a password reset." on public.password_resets for insert with check (true);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Admins can manage reset requests.' and tablename = 'password_resets') then
    create policy "Admins can manage reset requests." on public.password_resets for all using (
      exists (
        select 1 from public.profiles
        where id = auth.uid() and user_type = 'admin'
      )
    );
  end if;
end $$;

-- TRIGGER for automatic profile creation on signup
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, full_name, phone_number, district, user_type)
  values (
    new.id,
    new.raw_user_meta_data->>'full_name',
    new.raw_user_meta_data->>'phone_number',
    new.raw_user_meta_data->>'district',
    new.raw_user_meta_data->>'user_type'
  );
  return new;
end;
$$ language plpgsql security definer;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

